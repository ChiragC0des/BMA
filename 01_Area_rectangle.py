# Write a program that calculates the area of a rectangle. 
# The program should prompt the user to enter the length and width of the rectangle. 
# The program should then calculate and display the area of the rectangle. 

l = float(input("Enter the length of the rectangle in meters: "))
w = float(input("Enter the width of the rectangle in meters: "))

# Calculate the area of the rectangle
area = l * w

# Display the area of the rectangle
print("The area of the rectangle is: ", area, "square meters")
